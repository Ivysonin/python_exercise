# Faça um programa que peça para o usuário digitar duas frases e
# em seguida, exiba se elas são iguais ou diferentes.

frase1 = input ("Digite uma frase: ")
frase2 = input ("Dgite outra frase: ")

if frase1 != frase2 :
    print ("Essas frases são diferentes !")
else:
    print ("Essas frases são iguais !")
