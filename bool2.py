# Crie duas variáveis, a e b, com valores booleanos (True ou False).
# Escreva expressões que usem os operadores lógicos and, or e not 
# para combinar essas variáveis e imprimir os resultados.

a = True
b = False

print(a and b) # False: pq precisa que as duas sejam verdadeiras
print(a or b) # True: pq só precisa que somente uma seja verdadeira
print(not a) # É True e se transforma em False
print(not b) # É False e se transforma em True